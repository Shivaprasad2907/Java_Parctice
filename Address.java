class Address {
    String Hno;
    String Street;
    String City;
    String State;
    String Pincode;


    Address(String Hno, String Street, String City, String State, String Pincode) {
        this.Hno = Hno;
        this.Street = Street;
        this.City = City;
        this.State = State;
        this.Pincode = Pincode;

    }
}
